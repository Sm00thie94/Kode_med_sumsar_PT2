public class BooleanExercise {
    public static void main(String[] args) {
        int x = 8;
        boolean xCheck = x > 10;
        System.out.println(x);
        System.out.println(xCheck);

        int y = 10;
        boolean yCheck = y != 10;
        System.out.println(y);
        System.out.println(yCheck);

    }
}
